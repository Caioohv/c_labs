#include <stdio.h>

int main() {
  
  /*
    we have many types of variables
    variables are representation for values
    variables can be:
    - int (integer numbers)
    - float (decimal numbers)
    - char (characters)
    - bool (boolean, true or false)
    and many others :)
  */

  int age = 25;

  /*
    to print a variable, we use the
    %x structure
    % says it is an structure
    x says its type
    
  */

  printf("I am %d years old", age);

  return 0;
}